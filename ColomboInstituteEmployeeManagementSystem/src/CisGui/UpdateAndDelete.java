    private void tblEmployeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeMouseClicked
        int i = tblEmploye.getSelectedRow();
        TableModel model = tblEmploye.getModel();
        txtId.setText(model.getValueAt(i, 0).toString());
        txtEmpName.setText(model.getValueAt(i, 1).toString());
        txtAge.setText(model.getValueAt(i, 2).toString());
        txtPnumber.setText(model.getValueAt(i, 5).toString());
        txtEmail.setText(model.getValueAt(i, 3).toString());
        txtAddress.setText(model.getValueAt(i, 4).toString());
        String departmentValue = model.getValueAt(i, 6).toString();
         comDep.setSelectedItem(departmentValue);
         String designationValue = model.getValueAt(i, 7).toString();
        comDes.setSelectedItem(designationValue);
        txtEpf.setText(model.getValueAt(i, 8).toString());
        

        
       
        
    }//GEN-LAST:event_tblEmployeMouseClicked

