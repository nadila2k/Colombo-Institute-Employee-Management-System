    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
                int id =Integer.parseInt(txtId.getText());
                String empName = txtEmpName.getText();
                int age = Integer.parseInt(txtAge.getText());
                String email = txtEmail.getText();
                String address = txtAddress.getText();
                int phoneNum = Integer.parseInt(txtPnumber.getText());
                String department = comDep.getSelectedItem().toString();
                String designation = comDes.getSelectedItem().toString();
                int epfNo = Integer.parseInt(txtEpf.getText());
         
                String sql = "UPDATE employee SET empName='" + empName + "', age=" + age + ", email='" + email + "', address='" + address + "', phone=" + phoneNum + ", department='" + department + "', designation='" + designation + "', epfNo=" + epfNo + " WHERE id=" + id;
                if(stm.executeUpdate(sql)> 0){
                   JOptionPane.showMessageDialog(this, "Employee Record Updated");
                   }
                }catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Invalid User Input ");
                }           

    }//GEN-LAST:event_btnUpdateActionPerformed

